	<div class="drop-cap">“THE</div>
	<p class="first-line">
		reason that the rich were so rich, Vimes reasoned, was because they managed to spend less money.
	</p>
	<p></p>
	<p>
		Take boots, for example. He earned thirty-eight dollars a month plus allowances. A really good
		pair of leather boots cost fifty dollars. But an affordable pair of boots, which were sort of OK
		for a season or two and then leaked like hell when the cardboard gave out, cost about ten
		dollars. Those were the kind of boots Vimes always bought, and wore until the soles were so thin
		that he could tell where he was in Ankh-Morpork on a foggy night by the feel of the cobbles.
	</p>
	<p>
		But the thing was that good boots lasted for years and years. A man who could afford fifty
		dollars had a pair of boots that′d still be keeping his feet dry in ten years′ time, while the
		poor man who could only afford cheap boots would have spent a hundred dollars on boots in the
		same time and would still have wet feet.
	</p>
	<p>This was the Captain Samuel Vimes ‘Boots’ theory of socioeconomic unfairness.”</p>
	<p><span class="emdash">---</span> Sir Terry Prachett, Men at Arms</p>
